def standard_role_of(root, relpath):

    root_map = STANDARD_ROLE_MAP.get(root)
    if not root_map:
        return None
    key = relpath.replace("\\", "/").lower()
    return root_map.get(key)


def role_to_path(root):

    out = {}
    for p in STD_FILES.get(root, []):
        r = standard_role_of(root, p)
        if r and r not in out:
            out[r] = p
    return out


def role_to_paths(root):

    out = {}
    for p in STD_FILES.get(root, []):
        r = standard_role_of(root, p)
        if r:
            out.setdefault(r, []).append(p)
    return out


